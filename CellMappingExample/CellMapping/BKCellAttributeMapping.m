#import "BKCellAttributeMapping.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation BKCellAttributeMapping

@synthesize mappingType = _mappingType;
@synthesize keyPath = _keyPath;
@synthesize attribute = _attribute;
@synthesize valueBlock = _valueBlock;


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)dealloc {
    self.keyPath = nil;
    self.attribute = nil;
    self.valueBlock = nil;
    
    [super dealloc];
}


////////////////////////////////////////////////////////////////////////////////////////////////////
+ (id)attributeMapping {
    return [[[self alloc] init] autorelease];
}

@end
